package com.example.ikbq.entity;

public class Deptinfo
{
	private Long did;
	private String name;
	private String sum;

	public String getSum()
	{
		return sum;
	}

	public void setSum(String sum)
	{
		this.sum = sum;
	}

	public Long getDid()
	{
		return did;
	}

	public void setDid(Long did)
	{
		this.did = did;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}
}
